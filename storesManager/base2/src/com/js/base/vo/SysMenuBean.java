package com.js.base.vo;

public class SysMenuBean {
	protected String menu_no;
	protected String menu_name;
	protected String menu_upno;
	protected String menu_url;
	protected String menu_lvl;
	protected String menu_model;
	protected String menu_sts;

	public String getMenu_no() {
		return menu_no;
	}

	public void setMenu_no(String menu_no) {
		this.menu_no = menu_no;
	}

	public String getMenu_name() {
		return menu_name;
	}

	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}

	public String getMenu_upno() {
		return menu_upno;
	}

	public void setMenu_upno(String menu_upno) {
		this.menu_upno = menu_upno;
	}

	public String getMenu_url() {
		return menu_url;
	}

	public void setMenu_url(String menu_url) {
		this.menu_url = menu_url;
	}

	public String getMenu_lvl() {
		return menu_lvl;
	}

	public void setMenu_lvl(String menu_lvl) {
		this.menu_lvl = menu_lvl;
	}

	public String getMenu_model() {
		return menu_model;
	}

	public void setMenu_model(String menu_model) {
		this.menu_model = menu_model;
	}

	public String getMenu_sts() {
		return menu_sts;
	}

	public void setMenu_sts(String menu_sts) {
		this.menu_sts = menu_sts;
	}

}
