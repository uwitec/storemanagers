package com.js.base.vo;

public class SysRoleFuncBean {
	protected String func_no;
	protected String func_name;
	protected String mod_no;
	protected String func_url;
	protected String func_def;
	protected String func_sts;

	public String getFunc_no() {
		return func_no;
	}

	public void setFunc_no(String func_no) {
		this.func_no = func_no;
	}

	public String getFunc_name() {
		return func_name;
	}

	public void setFunc_name(String func_name) {
		this.func_name = func_name;
	}

	public String getMod_no() {
		return mod_no;
	}

	public void setMod_no(String mod_no) {
		this.mod_no = mod_no;
	}

	public String getFunc_url() {
		return func_url;
	}

	public void setFunc_url(String func_url) {
		this.func_url = func_url;
	}

	public String getFunc_def() {
		return func_def;
	}

	public void setFunc_def(String func_def) {
		this.func_def = func_def;
	}

	public String getFunc_sts() {
		return func_sts;
	}

	public void setFunc_sts(String func_sts) {
		this.func_sts = func_sts;
	}

}
