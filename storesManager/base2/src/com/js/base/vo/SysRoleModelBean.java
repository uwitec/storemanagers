package com.js.base.vo;

public class SysRoleModelBean {
	protected String mod_no;
	protected String mod_name;
	protected String mod_sts;

	public String getMod_no() {
		return mod_no;
	}

	public void setMod_no(String mod_no) {
		this.mod_no = mod_no;
	}

	public String getMod_name() {
		return mod_name;
	}

	public void setMod_name(String mod_name) {
		this.mod_name = mod_name;
	}

	public String getMod_sts() {
		return mod_sts;
	}

	public void setMod_sts(String mod_sts) {
		this.mod_sts = mod_sts;
	}

}
