package com.js.eoms.vo;

public class EomsResourceBean {
	protected String res_id;
	protected String res_kind;
	protected String res_cont;
	protected String res_url;
	protected String res_sts;
	protected String tx_date;
	protected String up_date;
	protected String op_no;

	public String getRes_id() {
		return res_id;
	}

	public void setRes_id(String res_id) {
		this.res_id = res_id;
	}

	public String getRes_kind() {
		return res_kind;
	}

	public void setRes_kind(String res_kind) {
		this.res_kind = res_kind;
	}

	public String getRes_cont() {
		return res_cont;
	}

	public void setRes_cont(String res_cont) {
		this.res_cont = res_cont;
	}

	public String getRes_url() {
		return res_url;
	}

	public void setRes_url(String res_url) {
		this.res_url = res_url;
	}

	public String getRes_sts() {
		return res_sts;
	}

	public void setRes_sts(String res_sts) {
		this.res_sts = res_sts;
	}

	public String getTx_date() {
		return tx_date;
	}

	public void setTx_date(String tx_date) {
		this.tx_date = tx_date;
	}

	public String getUp_date() {
		return up_date;
	}

	public void setUp_date(String up_date) {
		this.up_date = up_date;
	}

	public String getOp_no() {
		return op_no;
	}

	public void setOp_no(String op_no) {
		this.op_no = op_no;
	}
}
